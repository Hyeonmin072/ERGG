export function getTraitNameKr(code: number): string | null {
  if (!Number.isFinite(code)) return null;
  return TRAIT_NAME_KR_BY_CODE[Math.trunc(code)] ?? null;
}

export function getTraitImagePath(code: number): string | null {
  if (!Number.isFinite(code)) return null;
  const normalized = Math.trunc(code);

  // 능력치 증감 계열(79xxxxxx)은 현재 characteristic 이미지셋 대상이 아님.
  if (normalized >= 7900000) return null;

  const name = TRAIT_IMAGE_NAME_OVERRIDE_BY_CODE[normalized] ?? getTraitNameKr(normalized);
  if (!name) return null;
  return `/images/characteristic/${name}.webp`;
}
